package com.xd.github.ds;


public class Coordinate {

     Number x;
     Number y;

     public Coordinate(Number x, Number y) {
          this.x = x;
          this.y = y;
     }

     public Number getX() {
          return x;
     }

     public void setX(Number x) {
          this.x = x;
     }

     public Number getY() {
          return y;
     }

     public void setY(Number y) {
          this.y = y;
     }
}
